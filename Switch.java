public class Switch {
        public static void main(String[] args) {
                   char Grade ='B';
                   switch (Grade){
                    case 'A':
                               System.out.println("You got grade A");
                                break;
                    case 'B':
                                System.out.println("You got grade B");
                                break;
                    case 'C':
                                System.out.println("You got grade C");
                                break;
                    default:
                                System.out.println("re-enter");
                                break;
                    }                   
        }
}